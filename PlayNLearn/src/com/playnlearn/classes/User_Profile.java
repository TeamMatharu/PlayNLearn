package com.playnlearn.classes;

public class User_Profile {
	private int User_ID;
	private String User_Name;
	private String User_Progress;
	private byte[] User_Image;
	private String User_Star;
	private String User_Level;
	private String User_Email;
	private String User_Title;
	private String comment;

	public int getUser_ID() {
		return User_ID;
	}

	public void setUser_ID(int user_ID) {
		User_ID = user_ID;
	}

	public String getUser_Name() {
		return User_Name;
	}

	public void setUser_Name(String user_Name) {
		User_Name = user_Name;
	}

	public String getUser_Progress() {
		return User_Progress;
	}

	public void setUser_Progress(String user_Progress) {
		User_Progress = user_Progress;
	}

	public byte[] getUser_Image() {
		return User_Image;
	}

	public void setUser_Image(byte[] user_Image) {
		User_Image = user_Image;
	}

	public String getUser_Star() {
		return User_Star;
	}

	public void setUser_Star(String user_Star) {
		User_Star = user_Star;
	}

	public String getUser_Level() {
		return User_Level;
	}

	public void setUser_Level(String user_Level) {
		User_Level = user_Level;
	}

	public String getUser_Email() {
		return User_Email;
	}

	public void setUser_Email(String user_Email) {
		User_Email = user_Email;
	}

	public String getUser_Title() {
		return User_Title;
	}

	public void setUser_Title(String user_Title) {
		User_Title = user_Title;
	}

	public String getComment() {
		return comment;
	}

	public void setComment(String comment) {
		this.comment = comment;
	}

}
